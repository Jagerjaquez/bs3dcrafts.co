/**
 * Migrate admin password from plain text to bcrypt hash
 * Usage: node scripts/migrate-admin-password.js
 * 
 * This script reads ADMIN_SECRET from .env and generates ADMIN_PASSWORD_HASH
 */

require('dotenv').config()
const bcrypt = require('bcrypt')
const fs = require('fs')
const path = require('path')

const SALT_ROUNDS = 12

async function migratePassword() {
  try {
    const adminSecret = process.env.ADMIN_SECRET
    const adminPasswordHash = process.env.ADMIN_PASSWORD_HASH
    
    if (adminPasswordHash) {
      console.log('✅ ADMIN_PASSWORD_HASH already exists in .env')
      console.log('No migration needed.')
      
      // Verify the existing hash
      if (adminSecret) {
        const isValid = await bcrypt.compare(adminSecret, adminPasswordHash)
        console.log('Hash verification:', isValid ? 'VALID' : 'INVALID')
        
        if (isValid) {
          console.log('\n💡 You can now remove ADMIN_SECRET from your .env file')
          console.log('The system will use ADMIN_PASSWORD_HASH instead')
        }
      }
      return
    }
    
    if (!adminSecret) {
      console.error('❌ ADMIN_SECRET not found in .env file')
      console.log('Please set ADMIN_SECRET first, then run this migration')
      process.exit(1)
    }
    
    console.log('🔄 Migrating ADMIN_SECRET to ADMIN_PASSWORD_HASH...')
    console.log('Current password length:', adminSecret.length)
    
    // Generate hash
    const salt = await bcrypt.genSalt(SALT_ROUNDS)
    const hashedPassword = await bcrypt.hash(adminSecret, salt)
    
    // Verify the hash
    const isValid = await bcrypt.compare(adminSecret, hashedPassword)
    if (!isValid) {
      console.error('❌ Hash verification failed!')
      process.exit(1)
    }
    
    console.log('✅ Hash generated and verified successfully')
    console.log('\n=== ADD TO YOUR .ENV FILE ===')
    console.log(`ADMIN_PASSWORD_HASH="${hashedPassword}"`)
    
    // Try to automatically update .env file
    const envPath = path.join(process.cwd(), '.env')
    
    if (fs.existsSync(envPath)) {
      try {
        let envContent = fs.readFileSync(envPath, 'utf8')
        
        // Add the new hash variable
        if (!envContent.includes('ADMIN_PASSWORD_HASH=')) {
          envContent += `\n# Bcrypt hashed admin password (generated by migration)\nADMIN_PASSWORD_HASH="${hashedPassword}"\n`
          
          fs.writeFileSync(envPath, envContent)
          console.log('\n✅ .env file updated automatically')
          console.log('💡 You can now remove ADMIN_SECRET from your .env file')
          console.log('The system will use ADMIN_PASSWORD_HASH instead')
        }
      } catch (error) {
        console.log('\n⚠️  Could not automatically update .env file')
        console.log('Please add the ADMIN_PASSWORD_HASH manually')
      }
    }
    
    console.log('\n=== MIGRATION COMPLETE ===')
    console.log('Your admin password is now securely hashed with bcrypt')
    
  } catch (error) {
    console.error('❌ Migration failed:', error)
    process.exit(1)
  }
}

migratePassword()